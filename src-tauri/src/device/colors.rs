#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Push3Color {
    Off = 0,
    White = 3,
    Peach = 8,
    Coral = 4,
    Red = 5,
    Orange = 9,
    Amber = 12,
    Yellow = 13,
    Lime = 16,
    Chartreuse = 17,
    Green = 21,
    Mint = 29,
    Teal = 33,
    Cyan = 37,
    Sky = 41,
    Blue = 45,
    Indigo = 48,
    Purple = 49,
    Magenta = 52,
    Rose = 56,
    Pink = 57,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct Push3RgbColor {
    pub red: u8,
    pub green: u8,
    pub blue: u8,
}

impl Push3Color {
    pub fn device_value(self) -> u8 {
        self as u8
    }
}

pub fn map_pad_color_id(color_id: PadColorId) -> Push3Color {
    match color_id {
        PadColorId::Off => Push3Color::Off,
        PadColorId::White => Push3Color::White,
        PadColorId::Peach => Push3Color::Peach,
        PadColorId::Coral => Push3Color::Coral,
        PadColorId::Red => Push3Color::Red,
        PadColorId::Orange => Push3Color::Orange,
        PadColorId::Amber => Push3Color::Amber,
        PadColorId::Yellow => Push3Color::Yellow,
        PadColorId::Lime => Push3Color::Lime,
        PadColorId::Chartreuse => Push3Color::Chartreuse,
        PadColorId::Green => Push3Color::Green,
        PadColorId::Mint => Push3Color::Mint,
        PadColorId::Teal => Push3Color::Teal,
        PadColorId::Cyan => Push3Color::Cyan,
        PadColorId::Sky => Push3Color::Sky,
        PadColorId::Blue => Push3Color::Blue,
        PadColorId::Indigo => Push3Color::Indigo,
        PadColorId::Purple => Push3Color::Purple,
        PadColorId::Magenta => Push3Color::Magenta,
        PadColorId::Rose => Push3Color::Rose,
        PadColorId::Pink => Push3Color::Pink,
    }
}

pub fn map_pad_color_rgb(color_id: PadColorId) -> Push3RgbColor {
    map_palette_value_rgb(map_pad_color_id(color_id).device_value())
}

pub fn map_palette_value_rgb(value: u8) -> Push3RgbColor {
    let (red, green, blue) = PUSH_PALETTE_RGB
        .get(value as usize)
        .copied()
        .unwrap_or((0, 0, 0));

    Push3RgbColor { red, green, blue }
}
